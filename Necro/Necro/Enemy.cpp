#include "Enemy.h"
//////////////////////////////////////////////
//コンストラクタ							
//戻り値：なし										
//////////////////////////////////////////////
Enemy::Enemy()
{
	position_x = 48;
	position_y = 48;
}

//////////////////////////////////////////////
//デストラクタ							
//戻り値：なし										
//////////////////////////////////////////////
Enemy::~Enemy()
{

}

//////////////////////////////////////////////
//コンストラクタ							
//戻り値：なし	
//引数１：Xの初期座標
//引数２：Yの初期座標
//////////////////////////////////////////////
Enemy::Enemy(int x, int y)
{
	position_x = x;
	position_y = y;
}
//////////////////////////////////////////////
//初期化関数								
//戻り値：なし								
//引数１：Xの初期位置						
//引数２：Yの初期位置			
//引数３：HPの初期数値	
//関数の詳細説明							
//////////////////////////////////////////////
void Enemy::Init(int x, int y, int hp)
{
	position_x = x;
	position_y = y;
	Enemy::hp = hp;
}

//////////////////////////////////////////////
//移動関数									
//戻り値：なし								
//引数１：Xの移動量							
//引数２：Yの移動量							
//関数の詳細説明							
//////////////////////////////////////////////
void Enemy::Move(int x, int y)
{
	position_x += x;
	position_y += y;
}
//////////////////////////////////////////////
//HP増減関数									
//戻り値：なし								
//引数１：HPの変動量												
//////////////////////////////////////////////
void Enemy::Hp_change(int value)
{
	hp += value;
}
